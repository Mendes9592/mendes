          <td class="bodyText" width="500"><p><strong>Programme de Formation en Gestion</strong></p>

			<p><strong>Semestre 1: Fondamentaux de la Gestion</strong></p>
			<ol>
			  <li>
			    <p><strong>Introduction &agrave; la Gestion</strong></p>
			    <ul>
			      <li>Comprendre les principes de base de la gestion et son r&ocirc;le dans les organisations.</li>
		        </ul>
		      </li>
			  <li>
			    <p><strong>Communication et Leadership</strong></p>
			    <ul>
			      <li>D&eacute;velopper des comp&eacute;tences en communication efficace et leadership pour diriger des &eacute;quipes.</li>
		        </ul>
		      </li>
			  <li>
			    <p><strong>Gestion des Ressources Humaines</strong></p>
			    <ul>
			      <li>Apprendre les concepts cl&eacute;s de la gestion des ressources humaines, y compris le recrutement et la gestion des performances.</li>
		        </ul>
		      </li>
			  <li>
			    <p><strong>&Eacute;thique en Gestion</strong></p>
			    <ul>
			      <li>Explorer les aspects &eacute;thiques de la prise de d&eacute;cision et de la gestion d'&eacute;quipes.</li>
		        </ul>
		      </li>
		    </ol>
			<p><strong>Semestre 2: Gestion Op&eacute;rationnelle</strong></p>
			<ol>
			  <li>
			    <p><strong>Gestion de Projet</strong></p>
			    <ul>
			      <li>Acqu&eacute;rir des comp&eacute;tences pratiques pour planifier, ex&eacute;cuter et cl&ocirc;turer des projets avec succ&egrave;s.</li>
		        </ul>
		      </li>
			  <li>
			    <p><strong>Gestion de la Qualit&eacute;</strong></p>
			    <ul>
			      <li>Comprendre les principes de la gestion de la qualit&eacute; et son impact sur la satisfaction du client.</li>
		        </ul>
		      </li>
			  <li>
			    <p><strong>Finance pour Non-Financiers</strong></p>
			    <ul>
			      <li>Introduire les bases de la gestion financi&egrave;re pour les gestionnaires non sp&eacute;cialis&eacute;s en finance.</li>
		        </ul>
		      </li>
			  <li>
			    <p><strong>Innovation et Gestion du Changement</strong></p>
			    <ul>
			      <li>Explorer les strat&eacute;gies d'innovation et les techniques de gestion du changement pour rester comp&eacute;titif.</li>
		        </ul>
		      </li>
		    </ol>
			<p><strong>Semestre 3: Strat&eacute;gie d'Entreprise</strong></p>
			<ol>
			  <li>
			    <p><strong>Analyse Strat&eacute;gique</strong></p>
			    <ul>
			      <li>Utiliser des outils d'analyse pour &eacute;valuer l'environnement concurrentiel et formuler des strat&eacute;gies efficaces.</li>
		        </ul>
		      </li>
			  <li>
			    <p><strong>Marketing Strat&eacute;gique</strong></p>
			    <ul>
			      <li>Comprendre les concepts cl&eacute;s du marketing strat&eacute;gique pour promouvoir la croissance de l'entreprise.</li>
		        </ul>
		      </li>
			  <li>
			    <p><strong>Gestion des Risques</strong></p>
			    <ul>
			      <li>Identifier, &eacute;valuer et g&eacute;rer les risques potentiels dans un contexte commercial.</li>
		        </ul>
		      </li>
			  <li>
			    <p><strong>Projet de Gestion Strat&eacute;gique</strong></p>
			    <ul>
			      <li>Appliquer les connaissances acquises &agrave; la r&eacute;alisation d'un projet de gestion strat&eacute;gique.</li>
	            </ul>
		      </li>
	        </ol>
		    <p>&nbsp;</p>
		    <p>&nbsp;</p>
		    <p><strong>Programme de Formation en Droit</strong></p>
		    <p><strong>Semestre 1: Fondamentaux du Droit</strong></p>
		    <ol>
		      <li>
		        <p><strong>Introduction au Syst&egrave;me Juridique</strong></p>
		        <ul>
		          <li>Comprendre les bases du syst&egrave;me juridique, les sources du droit et la hi&eacute;rarchie des normes.</li>
	            </ul>
	          </li>
		      <li>
		        <p><strong>Droit Constitutionnel</strong></p>
		        <ul>
		          <li>Examiner les principes fondamentaux de la constitution et la structure du gouvernement.</li>
	            </ul>
	          </li>
		      <li>
		        <p><strong>Droit des Contrats</strong></p>
		        <ul>
		          <li>&Eacute;tudier les &eacute;l&eacute;ments essentiels des contrats, les obligations et les responsabilit&eacute;s des parties.</li>
	            </ul>
	          </li>
		      <li>
		        <p><strong>Introduction au Droit P&eacute;nal</strong></p>
		        <ul>
		          <li>Explorer les principes du droit p&eacute;nal et les fondements de la responsabilit&eacute; p&eacute;nale.</li>
	            </ul>
	          </li>
	        </ol>
		    <p><strong>Semestre 2: Sp&eacute;cialisation en Droit Civil</strong></p>
		    <ol>
		      <li>
		        <p><strong>Droit des Obligations</strong></p>
		        <ul>
		          <li>Approfondir la compr&eacute;hension des diff&eacute;rentes obligations juridiques et des modes de responsabilit&eacute;.</li>
	            </ul>
	          </li>
		      <li>
		        <p><strong>Droit de la Famille</strong></p>
		        <ul>
		          <li>Examiner les lois r&eacute;gissant le mariage, le divorce, la filiation et les droits des membres de la famille.</li>
	            </ul>
	          </li>
		      <li>
		        <p><strong>Droit des Biens</strong></p>
		        <ul>
		          <li>&Eacute;tudier les r&egrave;gles concernant la propri&eacute;t&eacute;, la possession et la transmission des biens.</li>
	            </ul>
	          </li>
		      <li>
		        <p><strong>M&eacute;thodologie Juridique</strong></p>
		        <ul>
		          <li>D&eacute;velopper des comp&eacute;tences en recherche juridique, r&eacute;daction de documents l&eacute;gaux et plaidoirie.</li>
	            </ul>
	          </li>
	        </ol>
		    <p><strong>Semestre 3: Droit des Affaires</strong></p>
		    <ol>
		      <li>
		        <p><strong>Droit Commercial</strong></p>
		        <ul>
		          <li>Comprendre les r&egrave;gles r&eacute;gissant les transactions commerciales, contrats commerciaux et soci&eacute;t&eacute;s.</li>
	            </ul>
	          </li>
		      <li>
		        <p><strong>Droit des Soci&eacute;t&eacute;s</strong></p>
		        <ul>
		          <li>Examiner la constitution, le fonctionnement et la dissolution des soci&eacute;t&eacute;s.</li>
	            </ul>
	          </li>
		      <li>
		        <p><strong>Droit du Travail</strong></p>
		        <ul>
		          <li>&Eacute;tudier les relations entre employeurs et employ&eacute;s, y compris les contrats de travail et les litiges.</li>
	            </ul>
	          </li>
		      <li>
		        <p><strong>Clinique Juridique en Entreprise</strong></p>
		        <ul>
		          <li>Appliquer les connaissances acquises &agrave; des situations r&eacute;elles gr&acirc;ce &agrave; des &eacute;tudes de cas et des projets pratiques.</li>
                </ul>
	          </li>
            </ol>
		    <p>&nbsp;</p>
		    <p><strong>Programme de Formation en Ing&eacute;nierie Informatique</strong></p>
		    <p><strong>Semestre 1: Fondamentaux de l'Ing&eacute;nierie Informatique</strong></p>
		    <ol>
		      <li>
		        <p><strong>Introduction &agrave; l'Informatique</strong></p>
		        <ul>
		          <li>Comprendre les bases de l'informatique, les syst&egrave;mes d'exploitation, et les principes de l'algorithmique.</li>
	            </ul>
	          </li>
		      <li>
		        <p><strong>Programmation Fondamentale</strong></p>
		        <ul>
		          <li>Ma&icirc;triser les langages de programmation courants et d&eacute;velopper des comp&eacute;tences en r&eacute;solution de probl&egrave;mes.</li>
	            </ul>
	          </li>
		      <li>
		        <p><strong>Architecture des Ordinateurs</strong></p>
		        <ul>
		          <li>&Eacute;tudier la conception et l'organisation des syst&egrave;mes informatiques, y compris les processeurs et la m&eacute;moire.</li>
	            </ul>
	          </li>
		      <li>
		        <p><strong>Math&eacute;matiques Appliqu&eacute;es &agrave; l'Informatique</strong></p>
		        <ul>
		          <li>Appliquer les concepts math&eacute;matiques &agrave; des probl&egrave;mes informatiques, notamment l'alg&egrave;bre lin&eacute;aire et la th&eacute;orie des graphes.</li>
	            </ul>
	          </li>
	        </ol>
		    <p><strong>Semestre 2: D&eacute;veloppement Logiciel</strong></p>
		    <ol>
		      <li>
		        <p><strong>Structures de Donn&eacute;es et Algorithmes</strong></p>
		        <ul>
		          <li>Approfondir la compr&eacute;hension des structures de donn&eacute;es avanc&eacute;es et des algorithmes d'optimisation.</li>
	            </ul>
	          </li>
		      <li>
		        <p><strong>G&eacute;nie Logiciel</strong></p>
		        <ul>
		          <li>Explorer les m&eacute;thodologies de d&eacute;veloppement logiciel, la gestion de projet et les bonnes pratiques de codage.</li>
	            </ul>
	          </li>
		      <li>
		        <p><strong>Bases de Donn&eacute;es</strong></p>
		        <ul>
		          <li>Concevoir et g&eacute;rer des bases de donn&eacute;es, comprendre les syst&egrave;mes de gestion de base de donn&eacute;es (SGBD).</li>
	            </ul>
	          </li>
		      <li>
		        <p><strong>Interfaces Utilisateur et Exp&eacute;rience Utilisateur (UI/UX)</strong></p>
		        <ul>
		          <li>Concevoir des interfaces utilisateur conviviales et comprendre les principes de l'exp&eacute;rience utilisateur.</li>
	            </ul>
	          </li>
	        </ol>
		    <p><strong>Semestre 3: Sp&eacute;cialisation en R&eacute;seaux et S&eacute;curit&eacute;</strong></p>
		    <ol>
		      <li>
		        <p><strong>R&eacute;seaux Informatiques</strong></p>
		        <ul>
		          <li>&Eacute;tudier la conception, l'impl&eacute;mentation et la gestion des r&eacute;seaux informatiques.</li>
	            </ul>
	          </li>
		      <li>
		        <p><strong>S&eacute;curit&eacute; Informatique</strong></p>
		        <ul>
		          <li>Comprendre les principes de base de la s&eacute;curit&eacute; informatique, y compris la cryptographie et la pr&eacute;vention des attaques.</li>
	            </ul>
	          </li>
		      <li>
		        <p><strong>Syst&egrave;mes Distribu&eacute;s</strong></p>
		        <ul>
		          <li>Explorer la conception et la mise en &oelig;uvre de syst&egrave;mes informatiques distribu&eacute;s.</li>
	            </ul>
	          </li>
		      <li>
		        <p><strong>Projet de S&eacute;curit&eacute; Informatique</strong></p>
		        <ul>
		          <li>Appliquer les connaissances acquises &agrave; des projets pratiques de s&eacute;curit&eacute; informatique.</li>
	            </ul>
	          </li>
	        </ol>
